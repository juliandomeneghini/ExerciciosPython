num1 = int(input('Ditite um número inteiro: '))
num2 = int(input('Ditite um número inteiro: '))
real = int(input('Digite um número real: '))

dobro = (num1 * 2) + (num2 / 2)
soma_triple = (num1 * 3 + real)
triplo = (real ** 3)

print(f'O produto do dobro do primeiro com metade do segundo é {dobro}')
print(f'A soma do triplo do primeiro com o terceiro é {soma_triple}')
print(f'O terceiro elevado ao cubo é {triplo}')
